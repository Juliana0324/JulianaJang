
class Operator3 {
	public static void main(String[] args) {
		int i=10, j=12, k=10; 

		System.out.println(i+" == "+ k + "="+ (i==k));
		System.out.println(i+" == "+ j + "="+ (i==j));
	
		//부정연산자
		System.out.println(i+" != "+ k + "="+ (i!=k));
		System.out.println(i+" != "+ j + "="+ (i!=j));

		//논리연산자
		//&& - (AND) : 전항 후항 모두 true일때만 true반환
		
	}
}
