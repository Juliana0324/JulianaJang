
class Variable{
	public static void main(String[] args){
	//정수
	int num = 24;
	
	//실수
	float fNum = 8.10f;
	double dNum= 9.123;

	//문자
	char txt = 'J';
	
	//문자열
	String str = "Hello My Name is Julie";

	System.out.println("정수: "+num);
	System.out.println("실수 Float: "+fNum);
	System.out.println("실수 Double: "+dNum);
	System.out.println();
	System.out.println("문자 : "+txt);
	System.out.println("문자열 : "+str);	
	
	}

}